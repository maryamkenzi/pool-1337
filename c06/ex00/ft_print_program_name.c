#include <unistd.h>

int	main(int arc, char **arv)
{
	int	i;

	i = 0;
	if (arc == 1)
	{
		while (arv[0][i])
		{
			write(1, &arv[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
